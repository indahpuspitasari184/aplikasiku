    private void isitable() {
        DefaultTableModel isitable = new DefaultTableModel();
        isitable.addColumn("ID");
        isitable.addColumn("Nama");
        isitable.addColumn("User Name");
        isitable.addColumn("Password");
        isitable.addColumn("Alamat");
        isitable.addColumn("Nmr Telpn");
        isitable.addColumn("Jabatan");
        try {
            statement = konek.getConnection().createStatement();
            String sql = "Select * from karyawan ";
            ResultSet resultSet = statement.executeQuery(sql);
            while (resultSet.next()) {
                isitable.addRow(new Object[]{
                    resultSet.getString(1),
                    resultSet.getString(2),
                    resultSet.getString(3),
                    resultSet.getString(4),
                    resultSet.getString(5),
                    resultSet.getString(6),
                    resultSet.getString(7),});
            }
            table.setModel(isitable);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

