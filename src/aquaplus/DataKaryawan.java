    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

        if (id.getText().equals("") && nama.getText().equals("") && user.getText().equals("") && password.getText().equals("") && alamat.getText().equals("") && nmrtlpn.getText().equals("") && jabatan.getSelectedItem().equals("")) {
            JOptionPane.showConfirmDialog(null, "Masih Ada Data Yang Kosong");
        } else {
            try {
                statement = konek.getConnection().createStatement();
                String sql = "insert into karyawan (id,nama,user,password,alamat,nmrtlpn,jabatan) values ('" + id.getText() + "','" + nama.getText() + "','" + user.getText() + "','" + password.getText() + "','" + alamat.getText() + "','" + nmrtlpn.getText() + "','" + jabatan.getSelectedItem() + "');";
                statement.execute(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasi Disimpan");
               bersih();
            } catch (SQLException e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
        isitable();
    }//GEN-LAST:event_btnsimpanActionPerformed

