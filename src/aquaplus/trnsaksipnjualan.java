    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        try {
            int row = table.getSelectedRow();
            Date tanggals = new SimpleDateFormat("yyyy-MM-dd").parse(table.getModel().getValueAt(row, 3).toString());
            tanggal.setDate(tanggals);
            id.setText(table.getModel().getValueAt(row, 0).toString());
            kasir.setText(table.getModel().getValueAt(row, 1).toString());
            cmbidpelanggan.setSelectedItem(table.getModel().getValueAt(row, 4).toString());
            txtPelanggan.setText(table.getModel().getValueAt(row, 5).toString());
            cmbidbarang.setSelectedItem(table.getModel().getValueAt(row, 6).toString());
            txtBarang.setText(table.getModel().getValueAt(row, 7).toString());
            txtHargaBarang.setText(table.getModel().getValueAt(row, 8).toString());
            txtJumlah.setText(table.getModel().getValueAt(row, 9).toString());
            txtTotalHarga.setText(table.getModel().getValueAt(row, 10).toString());
            btnsimpan.setEnabled(false);
        } catch (ParseException ex) {
            Logger.getLogger(TransaksiPenjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableMouseClicked

