    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String date = df.format(tanggal.getDate());
        if (kasir.getText().equals("") && cmbidpelanggan.getSelectedItem().equals("") && txtPelanggan.getText().equals("") && cmbidbarang.getSelectedItem().equals("") && txtBarang.getText().equals("") && txtHargaBarang.getText().equals("") && txtJumlah.getText().equals("") && txtTotalHarga.getText().equals("")) {
            JOptionPane.showConfirmDialog(null, "Masih Ada Data Yang Kosong");
        } else {
            try {
                statement = konek.getConnection().createStatement();
                String sql = "insert into transaksipenjualan (kasir,tanggal,id_pelanggan,namapelanggan,id_barang,namabarang,harga,jumlah,total) values ('" + kasir.getText() + "','" + date + "'," + cmbidpelanggan.getSelectedItem()+ "','" + txtPelanggan.getText() + "','" + cmbidbarang.getSelectedItem()+ "','" + txtBarang.getText() + "','" + txtHargaBarang.getText() + "','" + txtJumlah.getText() + "','" + txtTotalHarga.getText() + "');";
                statement.execute(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasi Disimpan");
                bersih();

            } catch (SQLException e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
        isitable();
    }//GEN-LAST:event_btnsimpanActionPerformed

