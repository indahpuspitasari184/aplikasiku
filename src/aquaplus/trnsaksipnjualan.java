    private void cmbidpelangganItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbidpelangganItemStateChanged
        try {
            statement = konek.getConnection().createStatement();
            String sql = "select nama from pelanggan where id = " + cmbidpelanggan.getSelectedItem().toString();
            ResultSet rs = statement.executeQuery(sql);
            while (rs.next()) {
                txtPelanggan.setText(rs.getString("nama"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TransaksiPenjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbidpelangganItemStateChanged

