    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        try {
            int row = table.getSelectedRow();
            Date tanggals = new SimpleDateFormat("yyyy-MM-dd").parse(table.getModel().getValueAt(row, 2).toString());
            tanggal.setDate(tanggals);
            id.setText(table.getModel().getValueAt(row, 0).toString());
            namakasir.setText(table.getModel().getValueAt(row, 1).toString());
            penjualanpagi.setText(table.getModel().getValueAt(row, 3).toString());
            pengeluaranpagi.setText(table.getModel().getValueAt(row, 4).toString());
            totalpengeluaranpagi.setText(table.getModel().getValueAt(row, 5).toString());
            penjualanmalam.setText(table.getModel().getValueAt(row, 6).toString());
            pengeluaranmalam.setText(table.getModel().getValueAt(row, 7).toString());
            totalpengeluaranmalam.setText(table.getModel().getValueAt(row, 8).toString());
            total.setText(table.getModel().getValueAt(row, 9).toString());
            btnsimpan.setEnabled(false);
            } catch (ParseException ex) {
            Logger.getLogger(Penjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableMouseClicked

