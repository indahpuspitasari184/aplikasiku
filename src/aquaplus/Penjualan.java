    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String fendy = df.format(tanggal.getDate());
        if (namakasir.getText().equals("") 
                && penjualanpagi.getText().equals("") 
                && pengeluaranpagi.getText().equals("") 
                && totalpengeluaranpagi.getText().equals("") 
                && penjualanmalam.getText().equals("") 
                && pengeluaranmalam.getText().equals("") 
                && totalpengeluaranmalam.getText().equals("") 
                && total.getText().equals("")) {
            JOptionPane.showConfirmDialog(null, "Masih Ada Data Yang Kosong");
        } else {
            try {
                statement = konek.getConnection().createStatement();
                String sql = "insert into penjualan (namakasir,tanggal,penjualanpagi,pengeluaranpagi,totalpengeluaranpagi,penjualanmalam,pengeluaranmalam,totalpengeluaranmalam,total) values ('" + namakasir.getText() + "','" + fendy + "','" + penjualanpagi.getText() + "','" + pengeluaranpagi.getText() + "','" + totalpengeluaranpagi.getText() + "','" + penjualanmalam.getText() + "','" + pengeluaranmalam.getText() + "','" + totalpengeluaranmalam.getText() + "','" + total.getText() + "');";
                statement.execute(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasi Disimpan");
                bersih();
               
            } catch (SQLException e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
        isitable();
    }//GEN-LAST:event_btnsimpanActionPerformed

